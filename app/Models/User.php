class User extends Authenticatable implements LdapAuthenticatable
{
    use HasApiTokens;
    use HasFactory;
    use HasProfilePhoto;
    use HasTeams;
    use Notifiable;
    use TwoFactorAuthenticatable;
    use AuthenticatesWithLdap;

    /**
     * The attributes that are mass assignable.
     *
     * @var array
     */
    /*protected $fillable = [
        'name', 'email', 'password',
    ];*/
    protected $guarded = [];

    /**
     * The attributes that should be hidden for arrays.
     *
     * @var array
     */
    protected $hidden = [
        'password',
        'remember_token',
        'two_factor_recovery_codes',
        'two_factor_secret',
    ];

    /**
     * The attributes that should be cast to native types.
     *
     * @var array
     */
    protected $casts = [
        'email_verified_at' => 'datetime',
    ];

    /**
     * The accessors to append to the model's array form.
     *
     * @var array
     */
    protected $appends = [
        'profile_photo_url',
    ];

    public function workingTerms(){
        return $this->hasMany(WorkingTerm::class);
    }

    /**
     * get active working terms
     * @return mixed
     */
    public function activeWorkingTerms()
    {
        return $this->workingTerms()->active();
    }

    /**
     * @return \Illuminate\Database\Eloquent\Relations\BelongsToMany
     */
    public function employment()
    {
        return $this
            ->belongsToMany(Employment::class,'working_terms')
            ->where('active','=',1);
    }

    public function service()
    {
        return $this
            ->belongsToMany(Service::class,'working_terms')
            ->where('active','=',1);
        //return $this->hasOneThrough(Service::class,'activeWorkingTerms');

    }

    public function bonuses()
    {
        return $this->hasMany(Bonus::class);
    }

    public function periods()
    {
        return $this->hasMany(Period::class)->orderByDesc('created_at');
    }

}
